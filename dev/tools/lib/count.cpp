#include <string>
#include <fstream>

#include "gene.hpp"
#include "count.hpp"

// ============================================================================
// Constructors
// ============================================================================

// ----------------------------------------------------------------------------
// Default constructor
// ----------------------------------------------------------------------------

Count::Count()
{
}

// ----------------------------------------------------------------------------
// Minimal constructor
// ----------------------------------------------------------------------------

Count::Count(Gene gene)
{
	this->gene = gene;
	this->count = 0;
}

// ============================================================================
// Getters
// ============================================================================

// ----------------------------------------------------------------------------
// GetGene()
// ----------------------------------------------------------------------------

Gene Count::GetGene() const
{
	return gene;
}

// ----------------------------------------------------------------------------
// GetCount()
// ----------------------------------------------------------------------------

unsigned long long Count::GetCount() const
{
	return count;
}

// ============================================================================
// Methods
// ============================================================================

// ----------------------------------------------------------------------------
// Increment()
// ----------------------------------------------------------------------------

void Count::Increment()
{
	count++;
}

// ============================================================================
// Operators
// ============================================================================

// ----------------------------------------------------------------------------
// ostream
// ----------------------------------------------------------------------------

std::ostream& operator<<(std::ostream& out, const Count& Count)
{
	out << "(" << Count.GetGene() << ", " << Count.GetCount() << ")";
	return out;
}

// ----------------------------------------------------------------------------
// lesser than
// ----------------------------------------------------------------------------

bool operator<(const Count& count1, const Count& count2)
{
	return count1.GetGene() < count2.GetGene();
}


