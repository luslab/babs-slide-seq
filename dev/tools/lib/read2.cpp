#include "utils.hpp"
#include "read2.hpp"

#include <seqan/basic.h>
#include <seqan/sequence.h>
#include <iostream>

// ============================================================================
// Constructors
// ============================================================================

// ----------------------------------------------------------------------------
// Default constructor
// ----------------------------------------------------------------------------

Read2::Read2()
{
}

// ----------------------------------------------------------------------------
// Minimal constructor
// ----------------------------------------------------------------------------

Read2::Read2(char* meta, char* sequence, char* quality)
{
	this->meta = std::string(meta);
	this->sequence = std::string(sequence);
	this->quality = std::string(quality);
}

// ============================================================================
// Getters
// ============================================================================

// ----------------------------------------------------------------------------
// GetMeta()
// ----------------------------------------------------------------------------

std::string Read2::GetMeta()
{
	return this->meta;
}

// ----------------------------------------------------------------------------
// GetSeq()
// ----------------------------------------------------------------------------

std::string Read2::GetSeq()
{
	return this->sequence;
}

// ----------------------------------------------------------------------------
// GetQual()
// ----------------------------------------------------------------------------

std::string Read2::GetQual()
{
	return this->quality;
}

// ============================================================================
// Operators
// ============================================================================

// ----------------------------------------------------------------------------
// ostream
// ----------------------------------------------------------------------------

std::ostream& operator<<(std::ostream& out, Read2& read2)
{
	out << "(" << read2.GetMeta() << ")";
	return out;
}

